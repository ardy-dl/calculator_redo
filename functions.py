class Operation:
    def addition(self, num_1, num_2):
        sum = num_1 + num_2
        return sum
    
    def subtraction(self, num_1, num_2):
        diff = num_1 - num_2
        return diff
    
    def multiplication(self, num_1, num_2):
        product = num_1 * num_2
        return product
    
    def division(self, num_1, num_2):
        if num_2 == 0:
            print("Error: Cannot divide by zero.")
        else:
            quotient = num_1 / num_2
            return quotient
        

    

        
